bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
#ifdef OLED_DRIVER_ENABLE
    set_keylog(keycode, record);
#endif
    // set_timelog();
  }

  switch (keycode) {
    case _3BACKT:
      if (record->event.pressed) {
        tap_code16(_BCKTIC);
        tap_code(KC_SPC);
        tap_code16(_BCKTIC);
        tap_code(KC_SPC);
        tap_code16(_BCKTIC);
        tap_code(KC_SPC);
      }
      return false;
    case _1BACKT:
      if (record->event.pressed) {
        tap_code16(_BCKTIC);
        tap_code(KC_SPC);
      }
      return false;
  }

  return true;
}
